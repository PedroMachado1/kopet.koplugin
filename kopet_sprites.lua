    sleeping = "(-.-)",
    eating   = "(@.@)",
    sick     = "(x_x)",
}

--------------------------------------------------------------------------------
-- Helper: get the correct stage details based on level and path
--------------------------------------------------------------------------------
function Sprites.get_stage(level, path)
    path = path or "normal"
    local base_stage = Sprites.stages[1]
    for _, s in ipairs(Sprites.stages) do
        if level >= s.min_level then
            base_stage = s
        end
    end

    if base_stage.alt and base_stage.alt[path] then
        return {
            name = base_stage.alt[path].name,
            min_level = base_stage.min_level,
            states = base_stage.alt[path].states
        }
    end

    return base_stage
end

--------------------------------------------------------------------------------
-- Helper: get raw sprite lines
--------------------------------------------------------------------------------
function Sprites.get_sprite(level, mood, path)
    local stage = Sprites.get_stage(level, path)
    local state = mood or "idle"
    if stage.states[state] then
        return stage.states[state]
    end
    return stage.states.idle
end

--------------------------------------------------------------------------------
-- Helper: get mini face
--------------------------------------------------------------------------------
function Sprites.get_mini(mood)
    return Sprites.mini[mood] or Sprites.mini.idle
end

--------------------------------------------------------------------------------
-- Accessory and Background Compositing
--------------------------------------------------------------------------------
-- Applies environment and accessories on top of the 8-line 14-char sprite array.
function Sprites.apply_compositing(base_lines, accessory)
    local out = {}

    -- Enforce standard width/height (8 lines, 14 chars approx)
    -- We assume the base_lines are 8 strings of 14 chars each.
    for i, line in ipairs(base_lines) do
        -- pad to at least 14 chars
        while #line < 14 do line = line .. " " end
        out[i] = line
    end


    -- 2. Apply accessory overlay if present
    -- Hats go on top of head, etc. We just hardcode a visual override for standard forms.
    if accessory == "hat" then
        out[1] = string.sub(out[1], 1, 4) .. "_/-/_" .. string.sub(out[1], 10, 14)
    elseif accessory == "glasses" then
        out[2] = string.sub(out[2], 1, 4) .. "-o-o-" .. string.sub(out[2], 10, 14)
    elseif accessory == "wand" then
        out[3] = string.sub(out[3], 1, 11) .. "/* "
        out[4] = string.sub(out[4], 1, 11) .. "// "
    elseif accessory == "bowtie" then
        out[4] = string.sub(out[4], 1, 5) .. ">x<" .. string.sub(out[4], 9, 14)
    elseif accessory == "ribbon" then
        out[1] = string.sub(out[1], 1, 8) .. "&&" .. string.sub(out[1], 11, 14)
    end

    return out
end

return Sprites
